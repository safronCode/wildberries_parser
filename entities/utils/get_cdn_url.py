def _get_basket_number(volume: int) -> str:
    limits = [
        (143, "01"), (287, "02"), (431, "03"), (719, "04"), (1007, "05"),
        (1061, "06"), (1115, "07"), (1169, "08"), (1313, "09"), (1601, "10"),
        (1655, "11"), (1919, "12"), (2045, "13"), (2189, "14"), (2405, "15"),
        (2621, "16"), (2837, "17"), (3053, "18"), (3269, "19"), (3485, "20"),
        (3701, "21"), (3917, "22"), (4133, "23"), (4349, "24"), (4565, "25"),
        (4877, "26"), (5189, "27"), (5501, "28"), (5813, "29"), (6125, "30"),
        (6437, "31"), (6749, "32"), (7061, "33"), (7373, "34"), (7685, "35"),
        (7997, "36"), (8309, "37"), (8741, "38"), (9173, "39"), (9605, "40"),
        (10373, "41"), (11141, "42"), (11909, "43")
    ]

    for limit, r in limits:
        if 0 <= volume <= limit:
            return r
    return "44"

def get_cdn_url(article: int) -> str:
    return (f"https://basket-{_get_basket_number(article // 100000)}.wbbasket.ru/"
            f"vol{article // 100000}/"
            f"part{article // 1000}/"
            f"{article}/")
